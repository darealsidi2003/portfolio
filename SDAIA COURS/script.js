// ============ STATE ============
let visitedCities = new Set(JSON.parse(localStorage.getItem('sa_visited') || '[]'));

// ============ DOM REFS ============
const welcomeScreen = document.getElementById('welcomeScreen');
const mapPage      = document.getElementById('mapPage');
const startBtn     = document.getElementById('startBtn');
const backBtn      = document.getElementById('backBtn');
const modalOverlay = document.getElementById('modalOverlay');
const modalClose   = document.getElementById('modalClose');
const visitedCount = document.getElementById('visitedCount');
const cityTooltip  = document.getElementById('cityTooltip');
const ttName       = document.getElementById('ttName');
const toast        = document.getElementById('toast');

// ============ HELPERS ============
function showToast(msg, duration = 2800) {
  toast.textContent = msg;
  toast.classList.add('show');
  setTimeout(() => toast.classList.remove('show'), duration);
}
function saveVisited() {
  localStorage.setItem('sa_visited', JSON.stringify([...visitedCities]));
}
function updateVisitedCount() {
  visitedCount.textContent = visitedCities.size;
}

// ============ TRANSITION: Welcome → Map ============
startBtn.addEventListener('click', () => {
  welcomeScreen.classList.add('exiting');
  setTimeout(() => {
    welcomeScreen.style.display = 'none';
    mapPage.classList.remove('hidden');
    requestAnimationFrame(() => mapPage.classList.add('visible'));
  }, 750);
});

backBtn.addEventListener('click', () => {
  mapPage.classList.remove('visible');
  setTimeout(() => {
    mapPage.classList.add('hidden');
    welcomeScreen.style.display = '';
    welcomeScreen.classList.remove('exiting');
  }, 400);
});

// ============ CITY PINS INTERACTION ============
const cityGroups = document.querySelectorAll('.city-group');
let tooltipTarget = null;

cityGroups.forEach(group => {
  const cityId = group.dataset.city;
  if (!CITIES[cityId]) return;

  // Mark visited cities
  if (visitedCities.has(cityId)) group.classList.add('visited');

  // Tooltip on hover
  group.addEventListener('mouseenter', (e) => {
    tooltipTarget = group;
    const city = CITIES[cityId];
    ttName.textContent = city.name;
    cityTooltip.classList.remove('hidden');
    positionTooltip(e, group);
  });
  group.addEventListener('mousemove', (e) => positionTooltip(e, group));
  group.addEventListener('mouseleave', () => {
    cityTooltip.classList.add('hidden');
    tooltipTarget = null;
  });

  // Click → open modal
  group.addEventListener('click', () => {
    openCityModal(cityId);
    if (!visitedCities.has(cityId)) {
      visitedCities.add(cityId);
      group.classList.add('visited');
      saveVisited();
      updateVisitedCount();
    }
  });
});

function positionTooltip(e, group) {
  const map = document.getElementById('saMap');
  const rect = map.getBoundingClientRect();
  const stageRect = document.querySelector('.map-stage').getBoundingClientRect();
  const cx = group.querySelector('.pin-core');
  if (!cx) return;

  // Get SVG point position in screen space
  const svgPt = map.createSVGPoint();
  const circles = group.querySelectorAll('circle');
  let cxVal = 0, cyVal = 0;
  if (circles.length > 0) {
    cxVal = parseFloat(circles[0].getAttribute('cx'));
    cyVal = parseFloat(circles[0].getAttribute('cy'));
  }
  svgPt.x = cxVal; svgPt.y = cyVal;
  const screenPt = svgPt.matrixTransform(map.getScreenCTM());

  cityTooltip.style.left = (screenPt.x - stageRect.left) + 'px';
  cityTooltip.style.top  = (screenPt.y - stageRect.top) + 'px';
}

updateVisitedCount();

// ============ MODAL ============
let currentCityId = null;
let currentTab = 'overview';

function openCityModal(cityId) {
  const city = CITIES[cityId];
  if (!city) return;
  currentCityId = cityId;
  currentTab = 'overview';

  // Fill hero
  document.getElementById('modalHeroGrad').style.background = city.color;
  document.getElementById('mhEmoji').textContent = city.emoji;
  document.getElementById('mhName').textContent = city.name;
  document.getElementById('mhRegion').textContent = city.region;
  document.getElementById('mhTags').innerHTML = city.tags.map(t => `<span class="mh-tag">${t}</span>`).join('');

  // Reset tabs
  document.querySelectorAll('.mtab').forEach(t => t.classList.remove('active'));
  document.querySelector('.mtab[data-tab="overview"]').classList.add('active');

  renderTab('overview', city);

  modalOverlay.classList.remove('hidden');
  document.body.style.overflow = 'hidden';
}

function renderTab(tab, city) {
  const container = document.getElementById('modalTabContent');
  container.innerHTML = '';

  if (tab === 'overview') {
    const info = city.overview;
    container.innerHTML = `
      <p class="overview-desc">${info.desc}</p>
      <div class="info-grid">
        ${info.info.map(i => `
          <div class="info-card">
            <div class="info-card-label">${i.label}</div>
            <div class="info-card-value">${i.value}</div>
          </div>
        `).join('')}
      </div>
    `;
  } else if (tab === 'landmarks') {
    container.innerHTML = `
      <div class="landmarks-list">
        ${city.landmarks.map(lm => `
          <div class="landmark-item">
            <div class="lm-icon">${lm.icon}</div>
            <div class="lm-info">
              <div class="lm-name">${lm.name}</div>
              <div class="lm-desc">${lm.desc}</div>
            </div>
          </div>
        `).join('')}
      </div>
    `;
  } else if (tab === 'culture') {
    container.innerHTML = `
      <div class="culture-section">
        <h4>🍽️ المطبخ المحلي</h4>
        <div class="culture-tags">${city.culture.food.map(f => `<span class="culture-tag">${f}</span>`).join('')}</div>
      </div>
      <div class="culture-section">
        <h4>🎭 الفنون والحرف</h4>
        <div class="culture-tags">${city.culture.arts.map(a => `<span class="culture-tag">${a}</span>`).join('')}</div>
      </div>
      <div class="culture-section">
        <h4>📖 ثقافة وتراث</h4>
        <p class="culture-text">${city.culture.text}</p>
      </div>
    `;
  }
}

// Tabs
document.querySelectorAll('.mtab').forEach(tab => {
  tab.addEventListener('click', () => {
    document.querySelectorAll('.mtab').forEach(t => t.classList.remove('active'));
    tab.classList.add('active');
    currentTab = tab.dataset.tab;
    if (currentCityId) renderTab(currentTab, CITIES[currentCityId]);
  });
});

// Close modal
function closeModal() {
  modalOverlay.classList.add('hidden');
  document.body.style.overflow = '';
  currentCityId = null;
}
